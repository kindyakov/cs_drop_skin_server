/**
 * Конфигурация бизнес-логики проекта
 * Все важные проценты, коэффициенты и ограничения в одном месте
 */

/**
 * ПРОДАЖА СКИНОВ
 * Процент от рыночной цены, который получает пользователь при продаже скина обратно сайту
 *
 * Пример:
 * - Скин стоит 1000₽ (100000 копеек)
 * - При продаже пользователь получает 950₽ (95000 копеек)
 * - Комиссия сайта: 50₽ (5%)
 */
export const SELL_PRICE_PERCENTAGE = 0.95; // 95% от цены

/**
 * Рассчитать цену продажи скина (с учетом комиссии сайта)
 * @param marketPrice - Рыночная цена скина (в копейках)
 * @returns Цена продажи (в копейках)
 */
export const calculateSellPrice = (marketPrice: number): number => {
  return Math.floor(marketPrice * SELL_PRICE_PERCENTAGE);
};

/**
 * Получить процент комиссии сайта при продаже скина
 * @returns Процент комиссии (например, 5 для 5%)
 */
export const getSiteCommissionPercentage = (): number => {
  return (1 - SELL_PRICE_PERCENTAGE) * 100;
};
